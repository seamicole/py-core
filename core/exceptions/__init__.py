# ┌─────────────────────────────────────────────────────────────────────────────────────
# │ ERROR
# └─────────────────────────────────────────────────────────────────────────────────────


class Error(Exception):
    """A base class for exception subclasses"""

    # ┌─────────────────────────────────────────────────────────────────────────────────
    # │ __INIT__
    # └─────────────────────────────────────────────────────────────────────────────────

    def __init__(self, message: str) -> None:
        """Init Method"""

        # Set message
        self.message = message

        # Call super init
        super().__init__(message)
