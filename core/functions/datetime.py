import pytz

from datetime import datetime


# ┌─────────────────────────────────────────────────────────────────────────────────────
# │ UTC NOW
# └─────────────────────────────────────────────────────────────────────────────────────


def utc_now() -> datetime:
    """Returns the current datetime in the UTC timezone"""

    # Return now in UTC
    return datetime.now(pytz.timezone("UTC"))
